package Class07;

public class WhileLoopString {

	public static void main(String[] args) {
		String day = "Saturday";

		if (day.equals("Saturday")) {
			System.out.println("My fav java class");
		}
		while (day.equals("Saturday")) {
			System.out.println("My fav java class");
			day = "Sunday";

		}

	}

}
